public class ARFModel {
        private String cid;
	private String complaintstate;
	private String discommittee;
	private String taskforcehead;
	private String contact;
	private String timings;

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }

    public String getComplaintstate() {
        return complaintstate;
    }

    public void setComplaintstate(String complaintstate) {
        this.complaintstate = complaintstate;
    }

    public String getDiscommittee() {
        return discommittee;
    }

    public void setDiscommittee(String discommittee) {
        this.discommittee = discommittee;
    }

    public String getTaskforcehead() {
        return taskforcehead;
    }

    public void setTaskforcehead(String taskforcehead) {
        this.taskforcehead = taskforcehead;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getTimings() {
        return timings;
    }

    public void setTimings(String timings) {
        this.timings = timings;
    }
}
